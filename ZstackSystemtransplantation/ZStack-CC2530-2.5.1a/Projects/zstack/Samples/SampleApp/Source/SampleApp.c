/*********************************************************************
 * @fn      SampleApp_HandleKeys
 *
 * @brief   Handles all key events for this device.
 *
 * @param   shift - true if in shift/alt.
 * @param   keys - bit field for key events. Valid entries:
 *                 HAL_KEY_SW_2
 *                 HAL_KEY_SW_1
 *
 * @return  none
 */
void SampleApp_HandleKeys( uint8 shift, uint8 keys )
{

  static unsigned char keynumber=0;
  static unsigned char seg7table[16] = {
    /* 0       1       2       3       4       5       6      7*/
    0xc0,   0xf9,   0xa4,   0xb0,   0x99,   0x92,   0x82,   0xf8,
    /* 8       9      A        B       C       D       E      F*/
    0x80,   0x90,   0x88,   0x83,   0xc6,   0xa1,   0x86,   0x8e };

  P1DIR = 0xff;
  P0 |= (0x1<<4);     // πƒ‹74HC573

  (void)shift;  // Intentionally unreferenced parameter

  if ( keys & HAL_KEY_SW_1 )
  {
    /* This key sends the Flash Command is sent to Group 1.
     * This device will not receive the Flash Command from this
     * device (even if it belongs to group 1).
     */
    SampleApp_SendFlashMessage( SAMPLEAPP_FLASH_DURATION );
  }

  if ( keys & HAL_KEY_SW_2 )
  {
    /* The Flashr Command is sent to Group 1.
     * This key toggles this device in and out of group 1.
     * If this device doesn't belong to group 1, this application
     * will not receive the Flash command sent to group 1.
     */
    aps_Group_t *grp;
    grp = aps_FindGroup( SAMPLEAPP_ENDPOINT, SAMPLEAPP_FLASH_GROUP );
    if ( grp )
    {
      // Remove from the group
      aps_RemoveGroup( SAMPLEAPP_ENDPOINT, SAMPLEAPP_FLASH_GROUP );
    }
    else
    {
      // Add to the flash group
      aps_AddGroup( SAMPLEAPP_ENDPOINT, &SampleApp_Group );
    }
  }

  if(keys & HAL_KEY_SW_6)
  {
    keynumber++;
    if(keynumber==10) keynumber=1;

    if(keynumber%2 != 0)
    {
       //HalLedOnOff(HAL_LED_2,HAL_LED_MODE_OFF);
      HalLedSet (HAL_LED_2, HAL_LED_MODE_OFF);
      HalLedBlink (HAL_LED_1, 2, 50, 1000);
    }
    else
    {
       //HalLedOnOff(HAL_LED_1,HAL_LED_MODE_OFF);
       //HalLedBlink (HAL_LED_2, 2, 50, 1000);
      HalLedSet (HAL_LED_1, HAL_LED_MODE_OFF);
      HalLedBlink (HAL_LED_2, 2, 50, 1000);
    }

    P1 =  seg7table[keynumber];

    //osal_start_timerEx(SampleApp_TaskID,SAMPLEAPP_EVT,3000);
  }


  P0 &= ~(0x1<<4);
  P0DIR &= ~0x10;  // …Ë÷√
}

